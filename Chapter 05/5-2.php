	echo"\$b=$b<br>"; 
	echo"比较的结果：<br>";
	//条件变量a大于 变量b。 
	if($a>$b)
	{ 
	 		echo"(\$a>\$b)即($(a)>$(b))为真！";
	 		echo"<br>";
	}
	else
	{
	 		echo"(\$a>\$b)即($(a)>$(b))为假";
