<?php
	 //此处变量a等于15,二进制码为1111. 
	$a=15;
	//这里将变量左移3位. 
	$b=$a<<3;
	//这里将变量右移3位. 
	$c=$a>>3;
	//在浏览器上输出a 的值. 
	echo"输出原值:$a";
	echo"<br>";
	//在浏览器上输出二进制码. 
	echo"二进制码为:".decbin($a);
	echo"<br>";
	//在浏览器上输出左移二位的值. 
	echo"左移三位:$b";
	echo"<br>";
	//在浏览器上输出b二进制码. 
	echo"二进制码:".decbin($b);
	echo"<br>";
	echo"右移三位:$c";
	?>
